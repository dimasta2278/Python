class Node:
    """
    Вспомогательный класс для узлов односвязного списка.
    Каждый узел хранит данные и ссылку на предыдущий узел.
    """
    def __init__(self, data):
        self.data = data      # данные узла
        self.pref = None      # ссылка на предыдущий узел


class Stack:
    """
    Основной класс для стека, реализованного на односвязном списке.
    В стеке доступ к элементам осуществляется по принципу LIFO (последним пришёл — первым вышел).
    """
    def __init__(self):
        self.end = None       # ссылка на последний добавленный элемент (вершина стека)

    def pop(self):
        """
        Извлекает последний элемент из стека, удаляя его.
        Возвращает значение удалённого элемента или None, если стек пуст.
        """
        if self.end is None:
            return None                 # стек пуст
        val = self.end.data              # сохраняем данные
        self.end = self.end.pref         # перемещаем вершину на предыдущий узел
        return val

    def push(self, val):
        """
        Добавляет новый элемент val в конец стека (на вершину).
        """
        new_node = Node(val)             # создаём новый узел
        new_node.pref = self.end         # новый узел ссылается на текущую вершину
        self.end = new_node              # обновляем вершину

    def print(self):
        """
        Выводит содержимое стека на печать (от вершины к началу).
        """
        current = self.end
        elements = []
        while current is not None:
            elements.append(str(current.data))
            current = current.pref
        print(" -> ".join(elements))     # пример: 3 -> 2 -> 1