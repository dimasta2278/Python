# Задача 2.
# Возводить в степень можно гораздо быстрее, чем за n умножений! Для этого нужно воспользоваться следующими рекуррентными соотношениями:
# a^n = (a^2)^n/2  при четном n,
# a^n = a × a^n−1 при нечетном n.
# На вход подается два числа через пробел: a, n. Напишите функцию для реализации алгоритма быстрого возведения в степень с помощью рекурсивной функции.
# Рекурсивная функция быстрого возведения в степень
def fast_pow(a, n):
    # Базовый случай: любое число в степени 0 равно 1
    if n == 0:
        return 1
    # Если степень чётная, используем соотношение a^n = (a^2)^(n/2)
    if n % 2 == 0:
        return fast_pow(a * a, n // 2)
    # Если степень нечётная, используем a^n = a * a^(n-1)
    else:
        return a * fast_pow(a, n - 1)

# Основная часть программы
if __name__ == "__main__":
    # Читаем два числа из одной строки
    a, n = map(int, input().split())
    # Выводим результат работы функции
    print(fast_pow(a, n))