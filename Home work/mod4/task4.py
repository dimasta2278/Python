# Напишите функцию для реализации: если из слова можно составить палиндром, то составить его и вывести на экран.
# Функция для попытки построить палиндром из строки
def make_palindrome(s):
    # Импортируем Counter для подсчёта символов (внутри функции, чтобы не засорять глобальное пространство)
    from collections import Counter
    # Считаем количество каждого символа
    cnt = Counter(s)
    # Собираем символы, которые встречаются нечётное количество раз
    odd_chars = [ch for ch, c in cnt.items() if c % 2 == 1]
    # Если таких символов больше одного, палиндром составить невозможно
    if len(odd_chars) > 1:
        return None  # нельзя составить палиндром
    # Формируем "половину" будущего палиндрома (без центрального символа)
    half = []
    for ch, c in cnt.items():
        # Берём половину каждого символа (целочисленное деление)
        half.append(ch * (c // 2))
    # Склеиваем все части половины
    half = ''.join(half)
    # Центральный символ — первый из нечётных (если они есть), иначе пустая строка
    middle = odd_chars[0] if odd_chars else ''
    # Возвращаем палиндром: половина + центр + перевёрнутая половина
    return half + middle + half[::-1]

# Основная часть программы
if __name__ == "__main__":
    # Считываем строку (слово)
    word = input().strip()
    # Пытаемся построить палиндром
    result = make_palindrome(word)
    # Если вернулся None, выводим сообщение об ошибке, иначе сам палиндром
    if result is None:
        print("Нельзя составить палиндром")
    else:
        print(result)