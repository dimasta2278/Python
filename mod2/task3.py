# Рассмотрим три числа
# a, b и c
# . Упорядочим их по возрастанию.
# Какое число будет стоять между двумя другими?
# Формат ввода
# В единственной строке записаны три целых числа
# a, b, c (−1000≤a,b,c≤1000), числа разделены одиночными пробелами.
# Формат вывода
# Выведите число, которое будет стоять между двумя другими после упорядочивания.
# Пример 1
# Ввод  	Вывод
# 1 2 3  	2
#
# Пример 2
# Ввод        	Вывод
# 1000 -1000 0	 0
#
# Пример 3
# Ввод  	Вывод
# 3 1 3	     3
# Средний элемент
s = input().strip()
# Находим позиции пробелов
space1 = s.find(' ')
space2 = s.find(' ', space1 + 1)
a = int(s[:space1])
b = int(s[space1+1:space2])
c = int(s[space2+1:])
# Вычисляем среднее
max_val = max(a, b, c)
min_val = min(a, b, c)
mid = a + b + c - max_val - min_val
print(mid)
