import math  # Импортируем модуль math для вычисления квадратного корня

# Ввод числа N от пользователя
try:
    N = int(input("Введите число N: "))
except ValueError:
    print("Ошибка: необходимо ввести целое число.")
    exit()

# Проверяем, чтобы N было неотрицательным (иначе квадратный корень из отрицательных чисел не определён в действительных числах)
if N < 0:
    print("Ошибка: N должно быть неотрицательным, чтобы квадратные корни были действительными.")
    exit()

# Создаём список чисел от N до N^2 включительно
# range(start, stop) не включает stop, поэтому используем stop = N**2 + 1
numbers = list(range(N, N**2 + 1))

# Используем генератор списка для создания списка квадратных корней
# Для каждого числа x в numbers вычисляем sqrt(x)
sqrt_numbers = [math.sqrt(x) for x in numbers]

# Выводим исходный список чисел
print("Список чисел от N до N^2:")
print(numbers)

# Выводим список квадратных корней
print("Список квадратных корней этих чисел:")
print(sqrt_numbers)