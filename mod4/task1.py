# Задача 1.
# На вход подается список чисел через пробел. Напишите функцию выводящую сообщение для списка чисел:
# 1) Все числа равны
# 2) Все числа разные
# 3) Есть равные и неравные числа
# Функция анализирует список чисел
def analyze_numbers(nums):
    # Преобразуем список в множество, чтобы оставить только уникальные значения
    s = set(nums)
    # Если в множестве только один элемент, значит все числа равны
    if len(s) == 1:
        return "Все числа равны"
    # Если размер множества совпадает с размером списка, все числа уникальны
    elif len(s) == len(nums):
        return "Все числа разные"
    # Иначе есть как повторяющиеся, так и уникальные
    else:
        return "Есть равные и неравные числа"

# Точка входа в программу
if __name__ == "__main__":
    # Считываем строку, разбиваем по пробелам, преобразуем в целые числа
    numbers = list(map(int, input().split()))
    # Вызываем функцию и выводим результат
    print(analyze_numbers(numbers))