# Ввод строки с числами, разделенными пробелами
input_string = input("Введите числа через пробел: ")

# Ввод числа K
try:
    K = int(input("Введите число K: "))
except ValueError:
    print("Ошибка: K должно быть целым числом.")
    exit()

# Преобразуем строку в список целых чисел
try:
    numbers = [int(x) for x in input_string.split()]
except ValueError:
    print("Ошибка: в строке должны быть только целые числа.")
    exit()

# Генератор списка: оставляем только числа, кратные K
# Проверяем K != 0, чтобы избежать деления на ноль
if K == 0:
    print("Ошибка: деление на ноль невозможно. K не может быть 0.")
    exit()

# Создаем список чисел, кратных K
multiples = [num for num in numbers if num % K == 0]

# Выводим результат
print("Числа, кратные K:")
print(multiples)