# найти в тексте все вхождения даты и времени в формате ГГГГ-ММ-ДД
# ЧЧ:ММ:СС. Учитываются только года из 21 века. Корректно должны распознаваться месяца, дни, часы, минуты и секунды.

import re

text = input("Введите текст: ")

# Регулярное выражение для поиска даты и времени в формате ГГГГ-ММ-ДД ЧЧ:ММ:СС
# Учитываются только года из 21 века (2000-2099)
pattern = r'20\d{2}-\d{2}-\d{2}\s\d{2}:\d{2}:\d{2}'

result = re.findall(pattern, text)

print(result)