# Ввод строки с фразами, разделенными символом ";"
input_string = input("Введите фразы через точку с запятой: ")

# Разбиваем строку на отдельные фразы по символу ";"
# strip() убирает лишние пробелы вокруг фраз, если они есть
phrases = [phrase.strip() for phrase in input_string.split(";")]

# Генератор списка для подсчета количества слов в каждой фразе
# Словом считается последовательность символов, разделенных пробелами
lengths = [len(phrase.split()) for phrase in phrases]

# Выводим исходный список фраз
print("Исходный список фраз:")
print(phrases)

# Выводим список количества слов в каждой фразе
print("Количество слов в каждой фразе:")
print(lengths)