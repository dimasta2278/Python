# Ввод списка цен (целые числа через пробел)
prices_str = input("Введите цены товаров через пробел: ")

# Ввод числа K (полные K рублей дают скидку)
try:
    K = int(input("Введите число K: "))
except ValueError:
    print("Ошибка: K должно быть целым числом.")
    exit()

# Ввод числа M (размер скидки)
try:
    M = int(input("Введите число M: "))
except ValueError:
    print("Ошибка: M должно быть целым числом.")
    exit()

# Проверка, чтобы K было положительным, иначе деление на ноль или отрицательный шаг
if K <= 0:
    print("Ошибка: K должно быть положительным числом.")
    exit()

# Преобразуем строку в список целых чисел
try:
    prices = [int(x) for x in prices_str.split()]
except ValueError:
    print("Ошибка: введите только целые числа через пробел.")
    exit()

# Генератор списка для расчета цен со скидкой
# Для каждой цены: вычисляем количество полных K рублей (price // K) и вычитаем M * это количество
discount_prices = [price - (price // K) * M for price in prices]

# Выводим исходный список цен
print("Цены до скидки:")
print(prices)

# Выводим список цен после скидки
print("Цены после применения скидки:")
print(discount_prices)